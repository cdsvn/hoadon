                The IValuate system is a precise and intuitive machine developed to evaluate the cosmetic grading of mobile devices. 
                It is an automated machine comprised of conveyors, motors, sensors, robotics and high resolution and precision imaging technology with a sophisticated software and AI technology built in, which enables it to detect scratches, chips, cracks, lifted and bent screens. 
                The interior of the machine is comprised of a non-reflective surface material, which is better suited as a background material for images of the device that are taken from above or any side angles. 
                In the first stage, the conveyor is configured to move device into the right spot on the base for the camera to take an image of the back side of the device for analysis. The conveyor moves device to the next stage where a robotic mechanism supports to raise the device to record images of top and bottom sides of the device. 
                Another robotic mechanism rotates the device 90 degree for recording images of left and right sides of the device. Last stage, a robotic mechanism flips the device upside down to record an image of the back side of the device. 
